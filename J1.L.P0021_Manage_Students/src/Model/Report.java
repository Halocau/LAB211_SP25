package Model;

/**
 *
 * @author Admin
 */
public class Report {

    private String studentName;
    private String courseName;
    private int totalCourse;

    public Report(String studentName, String courseName, int totalCourse) {
        this.studentName = studentName;
        this.courseName = courseName;
        this.totalCourse = totalCourse;
    }

    public Report() {
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public int getTotalCourse() {
        return totalCourse;
    }

    public void setTotalCourse(int totalCourse) {
        this.totalCourse = totalCourse;
    }

    @Override
    public String toString() {
        return "Report{" + "studentName=" + studentName + ", courseName=" + courseName + ", totalCourse=" + totalCourse + '}';
    }

    public void printData() {
        System.out.printf("%-15s|%-5s|%-5d", studentName, courseName, totalCourse);
    }
}
